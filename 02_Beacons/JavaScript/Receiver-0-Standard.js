radio.onDataPacketReceived( ({ signal, receivedNumber }) =>  {
    if (signal > -50) {
        basic.showIcon(IconNames.Yes)
    } else if (signal > -60) {
        basic.showLeds(`
            . # # # .
            # # # # #
            # # # # #
            # # # # #
            . # # # .
            `)
    } else if (signal > -80) {
        basic.showLeds(`
            . . # . .
            . # # # .
            # # # # #
            . # # # .
            . . # . .
            `)
    } else if (signal > -120) {
        basic.showLeds(`
            . . . . .
            . . # . .
            . # # # .
            . . # . .
            . . . . .
            `)
    } else {
        basic.showLeds(`
            . . . . .
            . . . . .
            . . # . .
            . . . . .
            . . . . .
            `)
    }
})
radio.setGroup(1)
basic.showIcon(IconNames.Diamond)
